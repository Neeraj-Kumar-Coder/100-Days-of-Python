# OOPs: Object Oriented Programming style

# Features:
# 1. Encapsulation: It refers to the bundling of data with the mechanisms or methods that operate on the data, or the limiting of direct access to some data, such as an object's components.
# 2. Inheritance: It is a feature or a process in which, new classes are created from the existing classes.
# 3. Abstraction: It is the process of hiding the internal details of an application from the outer world.
# 4. Polymorphism: It is one of the core concepts of object-oriented programming (OOP) and describes situations in which something occurs in several different forms.
